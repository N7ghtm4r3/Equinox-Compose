@Composable
@NonRestartableComposable
fun ChameleonText(
    text: String,
    modifier: Modifier = Modifier,
    fontSize: TextUnit = TextUnit.Unspecified,
    fontStyle: FontStyle? = null,
    fontWeight: FontWeight? = null,
    fontFamily: FontFamily? = null,
    letterSpacing: TextUnit = TextUnit.Unspecified,
    textDecoration: TextDecoration? = null,
    textAlign: TextAlign? = null,
    lineHeight: TextUnit = TextUnit.Unspecified,
    overflow: TextOverflow = TextOverflow.Clip,
    softWrap: Boolean = true,
    maxLines: Int = Int.MAX_VALUE,
    minLines: Int = 1,
    onTextLayout: ((TextLayoutResult) -> Unit)? = null,
    style: TextStyle = LocalTextStyle.current,
    backgroundColor: Color
) {
    Text(
        text = text,
        modifier = modifier,
        color = getContrastingColor(
            backgroundColor = backgroundColor
        ),
        fontSize = fontSize,
        fontStyle = fontStyle,
        fontWeight = fontWeight,
        fontFamily = fontFamily,
        letterSpacing = letterSpacing,
        textDecoration = textDecoration,
        textAlign = textAlign,
        lineHeight = lineHeight,
        overflow = overflow,
        softWrap = softWrap,
        maxLines = maxLines,
        minLines = minLines,
        onTextLayout = onTextLayout,
        style = style
    )
}

private fun getContrastingColor(
    backgroundColor: Color
): Color {
    val redRate = RED_PERCEPTION * backgroundColor.red
    val greenRate = GREEN_PERCEPTION * backgroundColor.green
    val blueRate = BLUE_PERCEPTION * backgroundColor.blue
    val luminance = ((redRate + greenRate + blueRate) * 255)
    return if (luminance > 128)
        Color.Black
    else
        Color.White
}